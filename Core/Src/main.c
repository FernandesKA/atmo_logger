/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* MPU Configuration--------------------------------------------------------*/
  MPU_Config();

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  /* MX_I2C2_Init(); */ /* SCD41 now on I2C1 */
  MX_ICACHE_Init();
  /* USER CODE BEGIN 2 */
  if (!oled_init(&hi2c1))
  {
    Error_Handler();
  }

  oled_clear();
  oled_draw_text(0, 0, "SCD41 Logger");
  oled_draw_text(0, 1, "I2C1: OLED");
  oled_draw_text(0, 2, "I2C1: SCD41");
  if (!oled_update())
  {
    Error_Handler();
  }

  if (!scd41_start_periodic(&hi2c1))
  {
    Error_Handler();
  }

  /* SCD41 datasheet: wait 5 seconds for first measurement in periodic mode */
  HAL_Delay(5000);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
    if (scd41_read_measurement(&hi2c1, &scd41_co2, &scd41_temperature, &scd41_humidity))
    {
      int temp_x10 = (int)(scd41_temperature * 10.0f + (scd41_temperature >= 0 ? 0.5f : -0.5f));
      int hum_x10 = (int)(scd41_humidity * 10.0f + 0.5f);
      int temp_whole = temp_x10 / 10;
      int temp_frac = temp_x10 >= 0 ? temp_x10 % 10 : - (temp_x10 % 10);
      int hum_whole = hum_x10 / 10;
      int hum_frac = hum_x10 % 10;

      snprintf(oled_line1, sizeof(oled_line1), "CO2:%4u ppm", scd41_co2);
      snprintf(oled_line2, sizeof(oled_line2), "T:%3d.%1dC", temp_whole, temp_frac);
      snprintf(oled_line3, sizeof(oled_line3), "H:%3d.%1d%%", hum_whole, hum_frac);

      oled_clear();
      oled_draw_text(0, 0, oled_line1);
      oled_draw_text(0, 1, oled_line2);
      oled_draw_text(0, 2, oled_line3);
      if (!oled_update())
      {
        Error_Handler();
      }

      /* Measurement OK: blink */
      HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);
    }

    /* Poll every 150 ms */
    HAL_Delay(150);

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

